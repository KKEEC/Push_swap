#include "ft_utils.h"

int	ft_isvalidarg(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] == '+' || str[i] == '-' || str[i] == ' ' || (str[i] >= '0'
					&& str[i] <= '9')))
			return (0);
		if (str[i] == '+' || str[i] == '-')
		{
			if (!(str[i + 1] >= '0' && str[i + 1] <= '9'))
				return (0);
			i++;
		}
		while (str[i] >= '0' && str[i] <= '9')
			i++;
		while (str[i] == ' ')
			i++;
	}
	return (1);
}
